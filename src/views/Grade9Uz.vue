 Раздел VII. Туркестан и Первая мировая война

 !Начало Первой мировой войны и ее влияние на Туркестан

Первая мировая война (1914–1918 гг.) оказала негативное влияние на экономику России и Туркестанского края.

   Причины войны: Обострение отношений между колониальными державами.
   Участие России: В составе союза Антанты (Англия, Франция).
   Влияние на экономику: Тяжелое экономическое положение России. Освоение природных ресурсов колоний (Туркестан) для поддержания экономики.
   Мобилизация: Призыв европейского населения края (19–43 года) на военную службу. Ухудшение положения семей мобилизованных.
   Чрезвычайное положение (с 1914 г.): Усиление надзора, ужесточение карательных мер. Штрафы и тюремное заключение для нарушителей.
   Последствия войны: Разорение сельского хозяйства, сокращение посевных площадей, нехватка рабочей силы. Прерывание экономических связей. Финансовый кризис государства. Сокращение финансирования просвещения и культуры. Увеличение налогов на 50%.
   Привлечение к тыловым работам: Указ Николая II от 25 июня 1916 г. о мобилизации мужчин Туркестана, Сибири, Кавказа (19–43 года) на тыловые работы.

Ключевые события, даты и личности:
   1914–1918 гг.: Первая мировая война.
   1914 г.: Объявление чрезвычайного положения в Туркестане.
   25 июня 1916 г.: Указ Николая II о мобилизации на тыловые работы.
   Личности: Николай II.

 !Восстания в Туркестане в 1916 году

Указ о мобилизации на тыловые работы вызвал массовое недовольство и восстания.

   Причины восстаний:
       Привлечение к тыловым работам (мардикорство).
       Лишение семей кормильцев, голод.
       Кризис сельского хозяйства.
       Несправедливое составление списков привлекаемых к работам (взятки, включение бедных семей).
       Злоупотребление властью местной администрацией.
   Ход восстаний:
       4 июля 1916 г.: Массовое восстание в Ходженте.
       5 июля: Выступления в кишлаках Ургут, Сияб, Махалля, Ходжа Ахрор, Ангор.
       7 июля: Выступления в Дахбеде.
       11 июля: Волнения в Ташкенте (перед полицейским управлением). Ранены Ризванбиби Ахмеджанова. Жестокое подавление. Введение осадного положения.
       Середина июля: Восстания в Кокандском, Риштанском, Маргиланском, Андижанском, Наманганском уездах Ферганской области.
       9 июля: Протест в Андижане.
       10 июля: Выступление в Старом Маргилане (25 тыс. человек).
       11 июля: Восстание в Намангане.
   Подавление: Жестокое подавление полицией и воинскими частями. Убийства, ранения мирных жителей (включая женщин и детей).
   Последствия: Серьезные потери населения, разорение селений, уничтожение урожая. Обострение жизни населения из-за дефицита продовольствия и роста цен. Восстания показали нарастающее национально-освободительное движение.

Ключевые события, даты и личности:
   Июль 1916 г.: Массовые восстания по всему Туркестану.
   4 июля 1916 г.: Восстание в Ходженте.
   11 июля 1916 г.: Волнения в Ташкенте.
   Личности: Николай II, А. Гиппиус, Ризванбиби Ахмеджанова.

 !Джизакское восстание

Восстание в Джизаке стало ареной борьбы против колониального гнета.

   Причины: Указ Николая II о мобилизации на тыловые работы, передача земель переселенцам, увеличение налогов, несправедливое составление списков привлекаемых к работам.
   Начало (3 июля 1916 г.): Недовольство после обнародования указа.
   Массовое выступление (5 июля): Требование отмены указа.
   Руководители: Назир Ходжи-ишан, Абдурахман-джевачи.
   Ход восстания (13 июля): Ремесленники потребовали от аксакала Мирзаяра Худаярова вернуть несправедливые списки. После отказа – нападение на новогородскую часть Джизака. Захват ж/д станций, перерезание линий связи.
   Подавление: Войска под командованием полковника Иванова (2,5 тыс. человек). Артиллерийский и пулеметный обстрел. Казнь лидеров (Абдурахман-джевачи, Назир Ходжи-ишан и др.). Разрушение домов, изгнание населения.
   Последствия: Жестокая расправа над участниками. Тысячи арестованных, приговоренных к смерти, тюремному заключению и ссылкам.

Ключевые события, даты и личности:
   3 июля 1916 г.: Начало недовольства в Джизаке.
   5 июля 1916 г.: Массовое выступление.
   13–21 июля 1916 г.: Бои и подавление восстания.
   Личности: Назир Ходжи-ишан, Абдурахман-джевачи, полковник Иванов, полковник Афанасьев.

 !Последствия событий 1916 года в Туркестане и их значение

Восстания 1916 года носили национально-освободительный характер, несмотря на поражение.

   Причины поражения: Отсутствие военного опыта и вооружения у восставших, тактическая и стратегическая неграмотность, разрозненность, отсутствие четкого плана, изоляция организации.
   Последствия:
       Серьезные потери народа: Сожжение селений, гибель урожая, нехватка продовольствия на зиму 1916–1917 гг.
       Ухудшение жизни населения из-за дефицита продовольствия и роста цен.
       Жестокое подавление восстаний.
   Значение:
       Признак нарастающего национально-освободительного движения.
       Показали колониальному правительству, что народ способен бороться за свободу.
       Убедили царское правительство в необходимости уступок.
       Борьба за свободу и независимость продолжалась.

Ключевые события, даты и личности:
   1916 г.: Восстания в Туркестане.
   Значение: Усиление национально-освободительного движения.
   Личности: А.Ф. Керенский (оценка обстановки).


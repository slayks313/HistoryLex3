 Раздел IX. Культурная жизнь Туркестана в колониальных условиях

 !Система образования. Печать

Система образования претерпела изменения под влиянием колониальной политики.

   Традиционная система образования: Формировалась на основе ислама. Обучение в школах и медресе (5–6 лет начальное, высшее – в медресе). Преподавались основы арабского языка, Коран, мусульманские традиции, фарси. Женщины обучались отдельно (атын-ойи). Государство не выделяло средств на традиционные школы.
   Русско-туземные школы: Открывались с целью русификации и подготовки переводчиков. Первая школа – в 1884 г. в доме Саидгани Саидазимбая. Методы преподавания ниже российских. Русский учитель (чтение, письмо, счет), местный учитель (вторая половина дня).
   Новомето́дные школы (джадидские): Основаны на национальной культуре, вере, обычаях. Преподавались история, математика, география, родной язык, история тюркских народов. Открывались в Бухаре, Ташкенте, Коканде, Андижане, Хиве. Цель – воспитание любви к Родине, национальной государственности.
   Борьба с новометодными школами: Колониальная администрация и консервативное духовенство препятствовали их деятельности. В 1911 г. закрыто около 50 школ, к 1913 г. в Бухаре – все джадидские школы.
   Печать:
       Официальные издания: «Туркестанские ведомости» (с 1870 г.), «Туркистон вилоятининг газети» (на узбекском). Редактор Н. Остроумов.
       Национальные газеты и журналы: «Тараккий» («Прогресс», 1906 г., редактор И. Абидов), «Шухрат», «Хуршид», «Ойна» (с 1913 г.), «Аль-изах» (с 1915 г.), «Садои Туркистон», «Садои Фаргона».

Ключевые события, даты и личности:
   Колониальный период: Изменения в системе образования.
   1870 г.: Выход «Туркестанских ведомостей».
   1884 г.: Открытие первой русско-туземной школы.
   1906 г.: Выход газеты «Тараккий».
   1911 г.: Закрытие около 50 джадидских школ.
   Личности: Г. Розенбах, Н. Остроумов, И. Абидов, Салохиддин домла, Шамсиддин домла, Маннон-кари.

 !Развитие науки в Туркестане

Джадиды способствовали развитию исторических и естественных наук.

   История: Джадиды считали, что историю создает народ, а не только правители.
       Труды: Мулла Алим Махдум Ходжи – «Тарихи Туркестан» (первое обзорное историческое произведение). Мухаммад Салих – «Тарихи жадидаи Тошкент» (история Ташкента XIX в.). Юсуф Баёни – «История Хорезма», «Генеалогия хорезмских шахов».
   Естественные науки:
       Картография: Генеральная карта края (1867 г.). Метеорологическая станция в Ташкенте.
       Исследования природных ресурсов: Н.А. Северцев (Памир), П.П. Семенов-Тянь-Шанский (Тянь-Шань), А.П. Федченко (Ферганская долина, Алай, Зарафшанская долина).
       Сельское хозяйство: Туркестанское сельскохозяйственное общество (журнал «Сельское хозяйство Туркестана»). Развитие хлопководства.
   Археология: Кружок любителей археологии (с 1895 г.). В. Вяткин (1869–1932) – востоковед, археолог, обнаружил остатки обсерватории Улугбека (1908 г.).
   Музеи и библиотеки: Туркестанский музей (1876 г., Ташкент), Туркестанская народная библиотека (1870 г., Ташкент).

Ключевые события, даты и личности:
   1867 г.: Составлена генеральная карта края.
   1870 г.: Открытие Туркестанской народной библиотеки.
   1876 г.: Создание Туркестанского музея.
   1895 г.: Создание Кружка любителей археологии.
   1908 г.: Обнаружение остатков обсерватории Улугбека.
   Личности: Мулла Алим Махдум Ходжи, Мухаммад Салих, Юсуф Баёни, В. Вяткин, Н.А. Северцев, П.П. Семенов-Тянь-Шанский, А.П. Федченко.

 !Театральное и музыкальное искусство

Развитие национального театра и музыки, влияние европейской культуры.

   Народный театр: Масхарабозы (клоуны, кукольники, танцоры), кызыкчи (комические актеры), кугырчакбазы (актеры кукольного театра). Сатирические сцены, высмеивающие пороки. Объединения: Закир-ишана (Коканд), Туля-масхары (Бухара). Кукольные театры (Качал-палван).
   Влияние европейского театра: Гастроли русских (Самарский театр – 1870 г., В.Ф. Комиссаржевская – 1910 г.), татарских и азербайджанских трупп. Музыкальные пьесы У. Гаджибекова («Гор-Оглы», «Лейли и Меджнун»).
   Национальный узбекский театр: Создан джадидами.
       Самарканд: Театр под руководством Махмудходжи Бехбуди. Первая пьеса – «Падаркуш» («Отцеубийца»).
       Ташкент: Театр «Турон» (1914 г.), основатель Абдулла Авлони. Первая постановка – «Падаркуш».
       Коканд: Театр самодеятельности Хамзы Хаким-заде Ниязи (1916 г.). Пьеса «Отравленная жизнь или жертвы любви».
   Музыка: Народные терма, кушик, лапары, ялла, ашула. Дастаны («Гор-Оглы», «Алпамыш»). Классические восточные макомы («Шашмаком»). Музыкальные инструменты: гиджак, кубуз, дутар, домбра, най, сурнай, доира.
   Хорезмская школа музыки: Покровительство хана Мухаммад Рахимхана II (Феруза).
   Первый узбекский фотограф: Худайберган Деванов (1878–1940).

Ключевые события, даты и личности:
   XIX в.: Развитие народного театра.
   1870 г.: Гастроли Самарского театра.
   1910–1912 гг.: Гастроли татарских и азербайджанских трупп.
   1911–1913 гг.: Создание первых национальных сценических произведений.
   1914 г.: Создание театра «Турон» в Ташкенте.

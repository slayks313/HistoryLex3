 Раздел IV. Национально-освободительное движение в Туркестане в конце XIX в.

 !Начало национально-освободительного движения в Туркестане и его предпосылки

Колониальная политика России подавляла национальное самосознание и свободу. Туркестан превратился в сырьевой придаток.

   Причины недовольства:
       Экономический гнет: Усиление торгово-ростовщического капитала, разорение крестьян, рост числа безземельных дехкан (чорикоры). Низкие цены на хлопок, высокие кредиты.
       Ухудшение положения ремесленников: Конкуренция с российскими товарами.
       Увеличение налогов и повинностей.
       Политическая зависимость, утрата национальной государственности.
       Нарушение местных традиций и обычаев.
   Движущие силы: Крестьяне, кустари-ремесленники, патриотически настроенное духовенство, собственники и торговцы.
   Формы протеста: Восстания, бунты, митинги.
   Примеры восстаний:
       1878 г.: Восстание в районе Мингтепа (предводитель Етимхан).
       Осень 1879 г.: Недовольство у Ферганского областного правления (требование снижения налогов).
       Ноябрь 1880 г.: Дополнительный земельный налог в Ходжентском и Уратюбинском районах (предводители Рахманкулходжа, Мирзакаримбай).
       Начало 1882 г.: Восстание в Намангане.
       Лето 1885 г.: Восстание в Ферганской долине (предводитель Дервишхан).
       1896 г.: Восстание в Аксу-Шахриханской волости Наманганского уезда.
   Карательные меры: Жестокое подавление выступлений, публичные казни, передача дел в военно-полевой суд.

Ключевые события, даты и личности:
   Конец XIX в.: Усиление колониального гнета.
   1878 г.: Восстание в Мингтепа.
   1880 г.: Дополнительный налог в Ходжентском уезде.
   1885 г.: Восстание в Ферганской долине.
   Личности: Етимхан, Рахманкулходжа, Мирзакаримбай, Дервишхан.

 !«Холерный бунт» в Ташкенте

Эпидемия холеры в 1892 году, наряду с усилением гнета, привела к народному восстанию.

   Причины восстания:
       Усиление политического, социально-экономического гнета.
       Тяжелое положение трудового народа, эксплуатация ремесленников и поденщиков.
       Рост цен на потребительские товары, увеличение налогов.
       Злоупотребление властью чиновниками, взяточничество.
       Слухи об умышленном отравлении воды властями во время эпидемии холеры.
       Смена аксакала Инагамходжи Умриеходжаева на Мухаммад Якуба, лояльного администрации.
   Начало восстания (24 июня 1892 г.): Направлено против зажиточной верхушки, чиновников-взяточников. Толпа (около 400 человек) направилась к начальнику города С. Путинцеву.
   Подавление: Применение силы, поджог дома аксакала, разгром канцелярии. Вызваны казачий полк и рота солдат.
   Последствия: Введение полицейского режима. «Правило о местах, где объявлено военное положение» (18 июня 1892 г.). Генерал-губернатор получил права запрещать собрания, закрывать предприятия, ссылать людей.

Ключевые события, даты и личности:
   Март 1892 г.: Начало эпидемии холеры в Афганистане.
   7 мая 1892 г.: Случаи холеры в Ташкенте.
   24 июня 1892 г.: Начало «холерного бунта».
   18 июня 1892 г.: Введение «Правила о местах, где объявлено военное положение».
   Личности: Инагамходжа Умриеходжаев, Мухаммад Якуб, С. Путинцев.

 !Андижанское восстание

Одно из наиболее значительных восстаний против колониального гнета в конце XIX в.

   Причины: Утверждение колониального режима (Положение 1886 г.), конфискация земель крестьян для переселенцев, политика монокультуры хлопка (сокращение посевов зерновых), рост цен, массовое переселение русских в Ферганскую долину, сокращение вакуфных земель, увеличение налогов, взяточничество, игнорирование национальных и религиозных ценностей.
   Начало волнений (весна 1898 г.): Распространение слухов о восстании под руководством Мухаммада Али (Дукчи-ишана).
   Сигнал к восстанию (17 мая 1898 г.): Перерезание телеграфных проводов в Таджик-кишлаке.
   Ход восстания: Восставшие напали на военную казарму в Андижане. Столкновение с превосходящими силами.
   Подавление: Жестокие меры царской администрации. Расстрелы мирного населения. Арест 2000 человек. Публичные казни (380 человек приговорены к казни, 22 казнены). Дукчи-ишан и его соратники повешены без суда.
   Последствия: Усиление контроля над населением. Восстание показало готовность народа бороться за свободу и независимость, заложило основу для дальнейшей борьбы.

Ключевые события, даты и личности:
   1898 г.: Андижанское восстание.
   17 мая 1898 г.: Сигнал к восстанию.
   Личности: Мухаммад Али (Дукчи-ишан), Зияуддин Махсум, полковник Коншевский, Н. Корольков.

 !Значение национально-освободительного движения

Национально-освободительное движение было логическим следствием колониальной политики.

   Причины: Тяжесть налогов и повинностей, предательское отношение местной власти, рост цен, ухудшение уровня жизни, русификация, лишение медицинского обслуживания, ориентация производства на нужды империи.
   Формы: Восстания, бунты, митинги.
   Характер: Национально-освободительный.
   Участники: Практически все народы края, представители разных слоев населения (крестьяне, ремесленники, улемы, аксакалы, торговцы).
   Значение:
       Продемонстрировало готовность народа бороться за свободу и независимость.
       Укрепило национальное самосознание.
       Показало колониальной державе, что народ не свернет с пути к свободе.
       Заложило фундамент для будущей борьбы за независимость Узбекистана.
   Лидеры: Пулатхан, Етимхан, Дервишхан, Дукчи-ишан.

Ключевые события, даты и личности:
   Вторая половина XIX в.: Период национально-освободительного движения.
   Значение: Борьба за национальную независимость и свободу.


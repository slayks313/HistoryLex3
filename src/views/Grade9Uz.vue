 Раздел VI. Движение джадидов и его значение в социально-экономической и культурной жизни Туркестана

 !Зарождение движения джадидов

Джадидизм возник как ответ на упадок, отсталость и попрание национальных ценностей, вызванные колониальной политикой.

   Предпосылки: Колониальная политика, направленная на поддержание неграмотности и отсталости местного населения. Невозможность открытой борьбы с царским правительством. Понимание интеллигенцией важности просвещения для достижения свободы.
   Основатель: Исмаил Гаспринский (1851–1914), крымско-татарский просветитель. Основал первую джадидскую школу в Бахчисарае (1884 г.). Разработал программу обучения.
   Термин «джадид»: В переводе с арабского – «новый». Джадиды выступали за реформу системы просвещения, преподавание светских наук наряду с религиозными.
   Идеи: Обучение молодежи в мусульманских школах исламу, арабскому, персидскому, русскому языкам, медицине, химии.
   Распространение: Газета «Таржимон» («Переводчик»). Визиты И. Гаспринского в Ташкент, Самарканд, Бухару (1893 г.). Открытие новометодных школ в Бухаре (1893 г.), Коканде (1898 г.), Андижане (1899 г.), Ташкенте.
   Этапы развития:
    1.  Просветительская деятельность: До 1917 г.
    2.  Политическое движение: С 1917 г.
   Региональные течения: Джадиды Туркестана, Бухары, Хивы.

Ключевые события, даты и личности:
   XIX в.: Формирование просветительского движения.
   1884 г.: Основание первой джадидской школы И. Гаспринским.
   1893 г.: Открытие первой новометодной школы в Бухаре.
   1910 г.: Возраст джадидов (Абдулхамид Чулпан – 13 лет, Махмудходжа Бехбуди – 36 лет).
   Личности: Исмаил Гаспринский, Абдулхамид Чулпан, Махмудходжа Бехбуди.

 !Джадиды: программные цели и задачи

Джадиды стремились к построению современного общества, борьбе с фанатизмом и отсталостью.

   Основные направления деятельности:
       Расширение сети новометодных школ.
       Обучение молодежи за рубежом.
       Создание просветительских обществ, театральных трупп.
       Издание газет и журналов.
       Формирование национального демократического государства через повышение общественно-политического и культурного сознания народа.
   Реформирование образования: Европейский стандарт обучения. Преподавание арифметики, истории, географии, природоведения, основ ислама. Создание новых учебников.
   Центры движения: Ташкент, Бухара, Самарканд, Андижан, Коканд, Хива.
   Количество школ: К 1911 г. – 63 школы, 4106 преподавателей. К 1917 г. – около 100 школ.
   Обучение женщин: Джадиды выступали за совместное обучение девочек и мальчиков, считая изучение веры долгом каждого.
   Программные цели:
       Освобождение от средневекового мракобесия и отсталости.
       Отмена старого метода обучения («Усули кадим»).
       Создание национального государства.
       Формирование свободного общества на основе конституционного правления.
       Придание тюркским языкам статуса государственного.
       Введение национальной валюты и армии.

Ключевые события, даты и личности:
   Начало XX в.: Активное развитие джадидского движения.
   1911 г.: 24 джадидские школы в Ташкенте (1740 детей).
   Цели: Просвещение, реформы, национальная государственность.

 !Представители джадидского движения и их деятельность

Джадиды внесли значительный вклад в развитие духовно-нравственной сферы и национальной культуры.

   Видные представители:
       Самарканд: Махмудходжа Бехбуди («отец джадидов Средней Азии»), Абдукадыр Шукури, Саидахмад Сиддики-Аджзи.
       Ташкент: Мунаввар Кары Абдурашидханов, Абдулла Авлони, Убайдуллаходжа Асадуллаходжаев.
       Бухара: Абдурауф Фитрат, Садриддин Айни, Файзулла Ходжаев.
       Ферганская долина: Хамза Хаким-заде Ниязи, Абиджан Махмудов, Абдулхамид Чулпан.
       Хива: Бабахун Салимов, Палваннияз Хаджи Юсупов.
   Деятельность: Открытие новометодных школ, библиотек, издательская деятельность (газеты «Бухоро-и-шариф», «Туран», «Шухрат», «Таржимон»), создание театральных трупп.
   Махмудходжа Бехбуди: Основатель и лидер джадидского движения в Средней Азии. Автор учебников («Адиби аввал», «Адиби соний»).
   Мунаввар Кары Абдурашидханов: Инициатор открытия школ, редактор газет, автор учебников.
   Абдулла Авлони: Основатель газеты «Шухрат», автор учебников, основатель театра «Турон».
   Общество «Тарбия-и атфол» («Воспитание детей»): Создано в 1910 г. для отправки молодежи на учебу за рубеж (Турция).

Ключевые события, даты и личности:
   Начало XX в.: Формирование поколения джадидов.
   1910 г.: Создание общества «Тарбия-и атфол».
   Личности: Махмудходжа Бехбуди, Мунаввар Кары, Абдулла Авлони, Абдурауф Фитрат, Файзулла Ходжаев, Хамза Хаким-заде Ниязи.

 !Влияние джадидского движения на общественно-политическую и культурную жизнь Туркестана

Джадиды стремились к реформам, основанным на прогрессивных идеях и правильном толковании ислама.

   Отношение к исламу: Джадиды придавали исламу большую роль в нравственном становлении. Выступали за унификацию догматов, правильное толкование Корана и хадисов, реформирование образования. Считали науку движущей силой прогресса.
   Общественно-экономическое развитие: Необходимость экономической самостоятельности государства. Объявление земли, недр, воды общим достоянием. Подготовка местных кадров, строительство заводов, развитие производства. Обучение молодежи за рубежом (Германия, Россия).
   Политическое движение: Превращение джадидизма в политическое течение. Требования: расширение прав местного населения, реформы управления, представительство в Государственной Думе, свобода печати.
   Создание партий: «Шурои Исломия» («Исламский совет»), «Иттифок» («Союз»).
   «Центральный совет мусульман» (май 1917 г.): Объединение свободолюбивых организаций Ташкента.
   Туркестанская Автономия (с центром в Коканде): Провозглашена джадидами после октябрьских событий 1917 г., просуществовала 72 дня.
   Последствия: Преследование джадидов, репрессии 1937–1938 гг.

Ключевые события, даты и личности:
   1909–1915 гг.: Создание трудов по истории ислама (Бехбуди, Авлони, Фитрат).
   1910–1917 гг.: Превращение джадидизма в политическое движение.
   Май 1917 г.: Создание «Центрального совета мусульман».
   Декабрь 1917 г.: Провозглашение Туркестанской Автономии.
   Личности: Махмудходжа Бехбуди, Абдулла Авлони, Абдурауф Фитрат, Убайдуллаходжа Асадуллаходжаев, Файзулла Ходжаев.


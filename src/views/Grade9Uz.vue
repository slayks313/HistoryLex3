 Раздел VIII. Бухарский эмират и Хивинское ханство в конце XIX – начале XX вв.

 !Государственное устройство и общественно-экономическая жизнь Бухарского эмирата

Соглашение 1868 года превратило эмират в протекторат России, но эмир сохранил неограниченную власть над подданными.

   Государственное устройство:
       Правитель: Эмир (Абдулахад, Музаффар).
       Контроль России: «Российское императорское политическое агентство» (1885–1917 гг.).
       Администрация: Верховный чиновник – кушбеги (контроль налогов, управление аппаратом). Диванбеги (казна, закят). Духовенство (судопроизводство, образование) во главе с главным казием. Раис (контроль законов, норм шариата).
       Территория: Делилась на 27 бекств, управляемых беками, назначаемыми эмиром.
       Войско (лашкар): Неорганизованное, плохо вооруженное, состояло из крестьян.
   Социально-экономическое положение:
       Население: Около 2 млн человек. Основные занятия – ремесло, земледелие, скотоводство.
       Налоги и повинности: Крестьяне платили около 90% всех налогов (харадж, закят, танобана). Увеличение налогов для покрытия расходов эмира и чиновников.
       Восстания:
           1885 г.: Восстание в Балджуване (предводитель Восе). Подавлено.
           1888 г.: Восстание в Кулябе.
           Май 1889 г.: Восстание в Калифе.
   Ремесло: Ковроткачество, изготовление кошмы, ювелирное дело, гончарное ремесло. Основная часть доходов ремесленников уходила на налоги и подаяния.
   Торговля: С Россией, Афганистаном, Индией, Ираном. Строительство железной дороги (1887 г.) связало Бухару с Оренбургом и Москвой. Появление русских деревень, банков, хлопкоочистительных заводов.

Ключевые события, даты и личности:
   1868 г.: Соглашение с Россией, установление протектората.
   1885–1917 гг.: Деятельность Российского политического агентства.
   1885 г.: Восстание Восе.
   1887 г.: Строительство железной дороги.
   Личности: Эмир Абдулахад, эмир Музаффар, кушбеги Мирзо Насрулла, Восе.

 !Деятельность младобухарцев и ликвидация Бухарского эмирата

В начале XX в. в Бухаре сформировались две силы: младобухарцы (сторонники реформ) и религиозные фанатики-консерваторы.

   Младобухарцы (джадиды): Начали деятельность в 1910 г. Стремились к демократической перестройке, развитию экономики, установлению республики. Издавали газеты «Бухоро-и-шариф», «Туран».
   Эмир Саид Алимхан (1910–1920 гг.): Изначально не сторонник реформ, но под влиянием событий в России и требований младобухарцев принял указ о реформах (март 1917 г.).
   Реформы эмира: Создание основ правосудия, упорядочение сбора налогов, развитие промышленности и торговли, фиксированная зарплата чиновникам, открытие типографии, освобождение заключенных.
   Реакция джадидов: Сомнения, разногласия. Радикальные сторонники (Файзулла Ходжаев, Абдурауф Фитрат) требовали немедленных преобразований.
   Демонстрации (8 апреля 1917 г.): В Керке и Бухаре. Подавлены сарбазами эмира. Аресты участников.
   Ликвидация эмирата:
       Январь 1920 г.: Создание «Центрального бюро революционеров-младобухарцев в Туркестане» (Ташкент).
       29 августа 1920 г.: Восстание в Чарджоу, создание Временного революционного комитета. Обращение к Российскому правительству за помощью.
       Сентябрь 1920 г.: Вход Красной армии в Бухару. Эмир Саид Алимхан бежал в Афганистан.
       6 октября 1920 г.: Провозглашение Бухарской Народной Советской Республики (БНСР). Первое правительство – Совет Народных Комиссаров во главе с Файзуллой Ходжаевым.

Ключевые события, даты и личности:
   1910 г.: Начало деятельности младобухарцев.
   Март 1917 г.: Указ эмира о реформах.
   8 апреля 1917 г.: Демонстрации младобухарцев.
   29 августа 1920 г.: Восстание в Чарджоу.
   2 сентября 1920 г.: Вход Красной армии в Бухару.
   6 октября 1920 г.: Провозглашение БНСР.
   Личности: Саид Алимхан, Файзулла Ходжаев, Махмудходжа Бехбуди, Абдурауф Фитрат.

 §!–36. Государственное устройство и социально-экономическое положение Хивинского ханства в конце XIX – начале XX вв.

Хивинское ханство находилось под жестким контролем России как протекторат.

   Государственное устройство:
       Правитель: Хан (Асфандиярхан, Мухаммад Рахимхан II).
       Контроль России: Чрезвычайно жесткий контроль. Совет (диван) из 7 человек (4 представителя царского правительства). Начальник Амударьинского отдела (правобережье Амударьи) – представитель России в ханстве.
       Администрация: Хан опирался на аристократию кунградского рода, дворцовую знать, духовенство. Хан обладал неограниченной властью. Войско (1,5 тыс. человек) – неорганизованное, примитивное вооружение.
       Территория: Делилось на 18 бекств и 2 ноибства.
   Социально-экономическое положение:
       Землевладение: Основная часть земли принадлежала хану, чиновникам, духовенству (вакуфные земли – около 40%). Крестьяне составляли 90% населения, владели 5% земель.
       Налоги и повинности: Множество видов налогов (салгут, закят, чуппули, утов) и повинностей (очистка каналов, ремонт дамб, мостов). Крестьяне-должники работали на кабальных условиях («яримчи»).
       Ремесло: Домашнее ремесло (прядение, ткачество, изготовление обуви, ковров). Развитие товарно-денежных отношений. Рост городов (Хива, Янги-Ургенч, Кунград).
       Промышленность: К 1909 г. – 81 предприятие. В 1910–1915 гг. – 40 предприятий (переработка керосина, нефти).
       Торговля: Развитие торговых отношений с Россией, Казахстаном, Кавказом, Ираном. Проникновение российского капитала. Синдикат «А-Мета» (1909 г.).
   Влияние России: Строительство Закаспийской железной дороги (1885 г.) и Амударьинской флотилии (1887 г.) усилили зависимость от России. Упадок кустарного ремесла из-за конкуренции с российскими товарами.

Ключевые события, даты и личности:
   1873 г.: Гандимянский договор, установление протектората.
   Конец XIX – начало XX вв.: Развитие промышленности и товарно-денежных отношений.
   1885 г.: Строительство Закаспийской железной дороги.
   1887 г.: Создание Амударьинской флотилии.
   1909 г.: Создание синдиката «А-Мета».
   Личности: Мухаммад Рахимхан II (Феруз), Асфандиярхан, Исламходжа, Джунаидхан.

 !Ликвидация Хивинского ханства

Нарастающее народное движение и вмешательство России привели к ликвидации ханства.

   Нарастание народного движения: Участились случаи недовольства, отказы от уплаты налогов, захваты земель, нападения на сборщиков налогов. Крестьянские объединения (80-е гг. XIX в.). Восстание под руководством Матъякуба-пишика (1902 г.).
   Вооружение восставших: Народ вооружился огнестрельным оружием и изучил тактику ведения боевых действий.
   Младохивинцы: Формирование джадидского движения. Участвовали разные слои населения. Представили хану «Проект реформ» (1910 г.).
   Хан Асфандиярхан (1910–1918 гг.): Усилил гнет, казнил верховного визиря Исламходжу. Подписал манифест о реформах (апрель 1917 г.).
   Меджлис (апрель 1917 г.): Избрание Совета назиров. Роспуск Меджлиса консервативными силами.
   Вмешательство Джунаидхана (январь 1918 г.): Занял Хиву, установил диктатуру, казнил Асфандиярхана. На престол взошел Саид Абдулла.
   Ликвидация ханства:
       Февраль 1920 г.: Создание Коммунистической партии в Петро-Александровске.
       27 апреля 1920 г.: I Всехивинский курултай советов провозгласил Хорезмскую Народную Советскую Республику (ХНСР).

Ключевые события, даты и личности:
   1902 г.: Восстание Матъякуба-пишика.
   1910 г.: «Проект реформ» младохивинцев.
   Апрель 1917 г.: Подписание манифеста о реформах, сессия Меджлиса.
   Январь 1918 г.: Джунаидхан занимает Хиву.
   27 апреля 1920 г.: Провозглашение ХНСР.
   Личности: Матъякуб-пишик, Асфандиярхан, Исламходжа, Джунаидхан, Саид Абдулла, Палваннияз ходжи Юсупов, Бобоахун Салимов.


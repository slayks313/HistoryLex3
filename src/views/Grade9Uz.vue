 !Архитектура и изобразительное искусство

Несмотря на колониальный гнет, сохранились национальные традиции зодчества и изобразительного искусства.

   Архитектура:
       Бухара и Хорезм: Развитие дворцовых ансамблей, медресе, мечетей, минаретов. Сохранение древних традиций с влиянием современных веяний. Мастера: Аблакул и Ибрахим Хафизовы, Муминджан Салихов, Уста-Ширин Мурадов.
       Хорезмская школа зодчества: Украшение порталов, потолков, башен резьбой и арабской вязью. Резьба по дереву (двери). Минарет Исламходжи (1908–1910 гг.).
       Ферганская долина: Проникновение русского архитектурного стиля. Андижанская соборная мечеть (конец XIX в.).
       Европейское влияние: Применение прямоугольного кирпича, фанеры, кровельного железа, стекла. Дворец Ситораи Мохи Хоса (Бухара, летняя резиденция эмира), дворец Нуруллабая (Хива, 1904–1912 гг.).
   Изобразительное искусство:
       Миниатюра: Мастера Самарканда, Коканда, Бухары (Ахмад Дониш, Абулхалик Махдум, С. Сиддыков).
       Настенные узоры: Появление пейзажей (корабли, железные дороги). Переход от религиозно-философского содержания к реальному изображению.
       Прикладное искусство: Резьба по ганчу (центр – Бухара), гончарные изделия, ювелирные изделия, роспись красок.

Ключевые события, даты и личности:
   Конец XIX – начало XX вв.: Развитие архитектуры и изобразительного искусства.
   Начало XX в.: Создание архитектурного ансамбля Ситораи Мохи Хоса. Реставрация минарета Исламходжи.
   1904–1912 гг.: Строительство дворца Нуруллабая.
   Личности: Уста-Ширин Мурадов, Адын Мухаммад Мурад, Нурмухаммад, Абдуджаббар, Исламходжа, Ахмад Дониш, Абулхалик Махдум, С. Сиддыков.


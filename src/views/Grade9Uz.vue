 Раздел V. Каракалпаки во второй половине XIX – начале XX вв.

 !Территориальное расселение и социальное положение каракалпаков во второй половине XIX – начале XX вв.

Каракалпаки, изначально жившие на берегах Урала и Волги, к началу XIX в. заняли низовья Амударьи и Приаралье. Подданство Хивинского ханства приняли из-за нападений казахов и туркмен.

   Расселение: Низовья Амударьи, южный и юго-западный берега Аральского моря.
   Образ жизни: Переход от кочевого к оседлому, основное занятие – земледелие, но скотоводство сохраняло ведущую роль. Рыболовство и охота также были важны.
   Социальное положение (середина XIX в.): Под властью Хивинского ханства. Население – около 100 тыс. человек. Земли – государственное имущество, платили налоги и повинности. Делились по родоплеменному признаку (аулы). Управление – «Каракалпакский улус» под руководством хана, аталыков, бегларбеги.
   Ремесло: Изготовление предметов быта (ткань для юрты, войлок), резьба по дереву и кости, ковроткачество, вышивка.
   Традиции и быт: Крепкие родоплеменные отношения (конгырат, китай, кыпчак, мангыт, кенагас). Управление – бии и старейшины. Важное место занимали свадебные и похоронные обряды. Эпос «Кырк кыз».

Ключевые события, даты и личности:
   Начало XIX в.: Занятие низовьев Амударьи.
   Середина XIX в.: Подданство Хивинского ханства, население около 100 тыс.
   Личности: Мухаммад Рахимхан II (сформировал «Каракалпакский улус»), Эрназарбий (предводитель восстания).
   Эпос: «Кырк кыз».

 !Налоги и повинности. Народные восстания

Каракалпаки, как и все подданные Хивы, платили налоги и несли повинности.

   Налоги: Земельный налог (салгут), налог духовенству (ушр), «котловые деньги» (в военное время), закят (с животноводства).
   Повинности: Рытье арыков (12 дней на мужчину из семьи), привлечение к воинской службе (1000–2000 воинов от общины).
   Народные восстания:
       1855 г.: Восстание под руководством Эрназарбия (каракалпакское племя кулдоли) против Хивинского ханства. Часть каракалпаков обратилась к России.
       1858–1859 гг.: Бунт в Кунграде, подавлен туркменским предводителем Атамурадханом.
       1881–1891 гг.: Восстание в Бийбазарской и Нукусской волостях (предводитель Баба-Гёклан).
       1900 г.: Народные волнения в Нукусской волости и Кунградском бекстве.

Ключевые события, даты и личности:
   XIX в.: Налоги (салгут, ушр, закят), повинности (рытье арыков, воинская служба).
   1855 г.: Восстание Эрназарбия.
   1881–1891 гг.: Восстание Баба-Гёклана.
   Личности: Эрназарбий, Баба-Гёклан, Мухаммад Аминхан, Атамурадхан.

 !Каракалпаки в составе Туркестанского генерал-губернаторства

После завоевания Хивинского ханства (1873 г.) и подписания Гандимянского договора, каракалпакские земли правобережья Амударьи вошли в состав Российской империи.

   Административное деление: Создан Амударьинский отдел в составе Сырдарьинской области Туркестанского генерал-губернаторства. Часть каракалпаков осталась в Хивинском ханстве.
   Население: В Амударьинском отделе – 70 тыс. каракалпаков (48,6% населения). Около 1 тыс. русских переселенцев (уральские казаки). В Хивинском ханстве – около 25 тыс. каракалпаков (3,8%).
   Социально-экономические изменения: Усиление влияния царского правительства. Более 70% каракалпаков – батраки. Развитие товарно-денежных отношений. Появление первых предприятий (хлопкоочистительные, рыбные). Развитие торговых отношений в Чимбае. Появление больницы и общеобразовательных школ.
   Социальное расслоение: Крупные землевладельцы (баи) отнимали земли и скот у крестьян. Расслоение по численности скота.

Ключевые события, даты и личности:
   1873 г.: Гандимянский договор, включение правобережья Амударьи в состав России.
   1870-е гг.: Переход к оседлому образу жизни, развитие земледелия (рис, пшеница, хлопок).
   1914 г.: 4 общеобразовательные школы, 2 больницы, 1 фельдшерский пункт.
   Личности: Мухаммад Рахимхан II (формирование «Каракалпакского улуса»).

 !Культура каракалпаков во второй половине XIX – начале XX вв.

Богатое поэтическое творчество, отражавшее народный талант.

   Устное народное творчество: Пословицы, предания, афоризмы, лапары, дастаны-эпосы («Кырк кыз» – героическая борьба за свободу). Главный герой-пересмешник – Омирбек-простофиля.
   Наука и образование: Несколько медресе (Каракум-ишан, Таш). Двухступенчатое обучение (арабский язык, исламские науки).
   Литература: Поэты Кунходжа (1799–1880), Аташ Алшинбай (1788–1875), Аджинияз (1824–1878, псевдоним Зевар), Бердах (1827–1900). Произведения: «Кырк кыз», «Бузатов», «Глупый шах», «Предшественники».
   Ремесло: Ковроткачество, изготовление войлока, резьба по дереву и кости, ювелирные изделия.

Ключевые события, даты и личности:
   XIX в.: Развитие устного народного творчества, литературы.
   Личности: Омирбек-простофиля, Гулаим, Кунходжа, Аташ Алшинбай, Аджинияз, Бердах.
   Произведения: «Кырк кыз», «Бузатов», «Глупый шах».


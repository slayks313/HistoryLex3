 Заключение

Вторая половина XIX – начало XX вв. – период завоевания Туркестана Российской империей, установления колониального деспотизма и попыток подчинения воли народа. Несмотря на тяжелые условия, народ сохранил свою культуру, традиции и стремление к свободе. Экономическое развитие края было односторонним, направленным на обеспечение нужд России. Национально-освободительное движение, несмотря на поражения, стало фундаментом для будущей независимости Узбекистана.

Ключевые события, даты и личности:
   XVIII в.: Взаимная вражда ханств способствовала завоеванию.
   Вторая половина XIX в.: Порабощение народов Туркестана.
   Колониальная политика: Направлена на установление деспотизма, уничтожение узбекской государственности.
   Экономика: Хлопкоперерабатывающая, горнорудная промышленность, железнодорожный транспорт созданы для нужд России. Кризис кустарно-ремесленного производства.
   Сохранение культуры: Народ сумел сохранить свою культуру, традиции, образ жизни.
   Стремление к свободе: Чувство независимости и свободы стало могучей силой для торжества независимости Узбекистана.
   Личности: Президент И.А. Каримов (цитаты о значении сохранения нации).

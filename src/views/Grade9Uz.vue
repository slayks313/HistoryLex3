 Раздел II. Завоевание Средней Азии Российской империей

 !Начало завоевательного движения Российской империи в Средней Азии

Россия стремилась расширить свои границы на Востоке. Промышленность империи нуждалась в дешевом сырье (хлопок) и рынках сбыта. Интерес Англии к Средней Азии ускорил российскую экспансию.

   Причины завоевания:
       Экономические: Потребность в хлопке-сырце, рынках сбыта.
       Политические: Расширение границ, геополитическое соперничество с Англией.
       Военные: Необходимость контроля над стратегическими территориями.
   Подготовка: Направление послов, сбор информации, военные экспедиции (А. Бекович-Черкасский при Петре I), строительство оборонительных укреплений (крепость Раим в 1847 г.).
   Английский интерес: Ост-Индская компания проявляла интерес к Средней Азии. Визиты Майкла Муркрофта, Александра Бёрнса, майора Иосифа Вольфа, капитана Джеймса Эббота. Попытки создания военного союза против России (Стоддарт и Конноли, казнены в 1842 г.).
   Ослабление ханств: Междоусобицы и внутренние распри истощили их экономический и военный потенциал.
   Этапы завоевания:
    1.  1847–1865 гг.: Захват северных вилоятов Кокандского ханства, Ташкента. Создание Туркестанской области (1865 г.).
    2.  1865–1868 гг.: Завоевание основной части Кокандского ханства и Бухарского эмирата.
    3.  1873–1879 гг.: Полное завоевание Хивинского и Кокандского ханств.
    4.  1880–1885 гг.: Покорение туркменских племен.

Ключевые события, даты и личности:
   1717 г.: Экспедиция А. Бековича-Черкасского.
   1847 г.: Строительство крепости Раим.
   1853 г.: Вторая попытка захвата Ак-мечети (форт Перовского).
   1864 г.: Захват Аулие-ата, Туркестана, Чимкента.
   1865 г.: Завоевание Ташкента.
   1864–1885 гг.: Период завоевания Средней Азии.
   Личности: Петр I, Александр II, А. Бекович-Черкасский, М. Черняев, Н. Веревкин.

 !Завоевание Ташкента

Ташкент был важным политическим, экономическим и торговым центром региона. Его завоевание обеспечивало России плацдарм для дальнейших военных действий.

   Значение Ташкента: Опорный пункт для военных действий, основной коридор для завоевания Средней Азии.
   Подготовка: Серьезная подготовка царского правительства.
   Первая атака (1 октября 1864 г.): М. Черняев отступил, потеряв солдат.
   Вторая атака (конец апреля 1865 г.): Захват крепости Ниязбек (ворота Ташкента), что позволило лишить город водоснабжения (разрушение плотины на Чирчике).
   Штурм Ташкента (май 1865 г.): Жестокие бои. Смертельное ранение командующего войсками Кокандского ханства Аликули. Запоздалая помощь Бухарского эмирата.
   Занятие города (17 июня 1865 г.): После ожесточенных боев и разрушений, под давлением, аксакалы подписали петицию о «добровольной сдаче». Несогласные сосланы в Сибирь.
   Последствия: Ташкент стал военным, политическим и административным центром Туркестанского края. Создание Туркестанской области в составе Оренбургского генерал-губернаторства. Военным губернатором назначен М. Черняев.

Ключевые события, даты и личности:
   Середина XIX в.: Ташкент – 12 ворот, 4 даха (района), население около 80 тыс.
   1 октября 1864 г.: Первая неудачная атака М. Черняева.
   Май 1865 г.: Захват крепости Ниязбек, начало штурма города.
   17 июня 1865 г.: Захват Ташкента.
   6 августа 1865 г.: Утверждение «Временного положения об управлении Туркестанской областью».
   Личности: М. Черняев, Аликули, Хакимходжа, Салихбек-дадхо.

 !Создание Туркестанского генерал-губернаторства

После завоевания Ташкента и создания Туркестанской области, царское правительство продолжило экспансию, стремясь установить стабильное управление и укрепить власть.

   Причины создания: Отдаленное расположение от Оренбурга, необходимость объединения будущих завоеванных земель, полное подчинение природно-географических и национальных богатств.
   Создание: В 1867 году из завоеванных территорий создается Туркестанское генерал-губернаторство и Туркестанский военный округ. Центр – Ташкент.
   Генерал-губернатор: Назначался императором, обладал широкими полномочиями (военная, гражданская власть, верховный судья). В 1867 г. назначен генерал-адъютант К.П. фон Кауфман (1867–1881 гг.), прозванный «полуцарем».
   Административное деление:
       Изначально: Сырдарьинская область (центр – Ташкент) и Семиреченская область (центр – Верный).
       Позднее (1868–1881 гг.): Зарафшанский округ (Самарканд), Амударьинская административная единица (Петро-Александровск), Ферганская область (Новый Маргилан), Закаспийская область (Ашхабад).
   Полномочия генерал-губернатора: Самостоятельно принимать решения, необходимые для управления краем, исходя из местных условий.

Ключевые события, даты и личности:
   1865 г.: Образование Туркестанской области.
   1867 г.: Создание Туркестанского генерал-губернаторства и Туркестанского военного округа.
   1867–1881 гг.: Генерал-губернатор К.П. фон Кауфман.
   1868 г.: Создание Зарафшанского округа.
   1873 г.: Создание Амударьинской административной единицы.
   1876 г.: Формирование Ферганской области.
   1881 г.: Создание Закаспийской области.

 !Начало военных действий против Бухарского эмирата

После завоевания Ташкента, следующим шагом стало поочередное завоевание ханств. Военные действия планировались в Ташкенте под лозунгом «Разделяй и властвуй».

   Предлог: Действия Бухарского эмирата против России.
   Начало военных действий: Д. Романовский (новый генерал-губернатор) начал занимать земли по среднему течению Сырдарьи.
   Боевые столкновения:
       8 мая 1866 г.: Битва у селения Ирджара. Поражение войск эмира.
       17 мая 1866 г.: Осада и штурм крепости Ходжент. Захват города 24 мая.
       Осень 1866 г.: Поход на Уратюбе (массированный артобстрел, штурм). Захват города.
       12 октября 1866 г.: Захват Джизака после двухдневного штурма.
   Последствия: Из территорий, завоеванных в 1864–1866 гг., образована Сырдарьинская область.

Ключевые события, даты и личности:
   1866 г.: Начало военных действий против Бухарского эмирата.
   8 мая 1866 г.: Битва у Ирджары.
   17–24 мая 1866 г.: Захват Ходжента.
   Осень 1866 г.: Захват Уратюбе и Джизака.
   1864–1866 гг.: Завоевание территорий, образование Сырдарьинской области.
   Личности: М. Черняев (отозван), Д. Романовский, Н. Крыжановский.

 !Установление протектората Российской империи над Бухарским эмиратом

Генерал фон Кауфман решил продолжить завоевание Бухарского эмирата, ослабленного междоусобицами.

   Причины: Отказ эмира Музаффара утвердить новый проект границ в пользу России.
   Военные действия (апрель 1868 г.): Завоевание Самарканда.
       1 мая 1868 г.: Битва у высот Чупан-аты. Поражение бухарских войск.
       Высоты Зирабулака: Второе сражение, поражение эмира.
   Захват Самарканда: Фактически без боя.
   Договор (23 июня 1868 г.):
       Самарканд отходил к России.
       Эмир Музаффар признавал себя виновным в развязывании войны.
       Уплата контрибуции в 500 тыс. рублей.
       Все завоеванные земли (от Ташкента до Самарканда: Ходжент, Уратюбе, Пенджикент, Джизак, Самарканд, Каттакурган) отходили к России.
       Эмир обязывался руководствоваться указаниями генерал-губернатора Туркестана.
       Защита интересов русских купцов (низкая пошлина – 2,5%).
   Последствия: Бухарский эмират стал протекторатом России, потеряв самостоятельность.
   Сопротивление: Народное ополчение Ургута, Каттакургана, Самарканда (под руководством Бабабека и Джурабека). Подавление сопротивления войсками Кауфмана.
   Новый договор (1873 г.): Россия получила право назначать своего представителя в Бухаре, без согласия которого эмир не мог решать внутренние и внешнеполитические вопросы. П. Лессар – первый представитель России в Бухаре (резиденция в Кагане).

Ключевые события, даты и личности:
   Апрель 1868 г.: Начало завоевания Самарканда.
   1 мая 1868 г.: Битва у Чупан-аты.
   23 июня 1868 г.: Подписание договора, установление протектората.
   1873 г.: Подписание нового договора.
   Личности: К.П. фон Кауфман, эмир Музаффар, генерал А. Абрамов, Бабабек, Джурабек, Абдумалик-тюра.

 !Установление протектората Российской империи над Хивинским ханством

Россия готовилась к завоеванию Хивинского ханства около пяти лет. Плацдармом стало завоевание Амударьи.

   Этап завоевания: Третий этап (1873–1879 гг.).
   Подготовка: Четыре военных отряда (Мангишлакский, Оренбургский, Туркестанский, Красноводский). Общая численность – более 12 тыс. солдат. Руководитель – К. Кауфман.
   Поход (февраль 1873 г.): Тяжелые условия (дороги, перепады температур, жара в пустынях). До Хивы добралось 7,5 тыс. солдат.
   Бои: В мае 1873 г. – бои у Хазараспской крепости, в городах Кунград, Ходжейли, Мангыт, у Хивы.
   Завоевание Хивы: К середине мая 1873 г. основные войска достигли Хивы. Первая атака отбита. 29 мая открыты ворота Хазараспа.
   Гандимянский договор (12 августа 1873 г.):
       Хан Мухаммад Рахимхан II признает себя подданным Императора Всероссийского.
       Отречение от налаживания связей с соседними правителями без разрешения России.
       Правый берег Амударьи отходил к России.
       Российским купцам – право прохода по Амударье, беспошлинная торговля.
       Право возводить пристани на левобережье Амударьи.
   Последствия: Хивинское ханство стало протекторатом России. Наложена контрибуция в 2,2 млн рублей. Территория сократилась втрое.

Ключевые события, даты и личности:
   1873–1879 гг.: Третий этап завоевания Средней Азии (Хивинское ханство).
   Май 1873 г.: Бои и подход к Хиве.
   29 мая 1873 г.: Завоевание Хивы.
   12 августа 1873 г.: Подписание Гандимянского договора.
   Личности: К. Кауфман, Мухаммад Рахимхан II, Н. Веревкин.

 !Завоевание Кокандского ханства

В 50–60-х гг. XIX в. Россия захватила часть Кокандского ханства. В 70-х гг. ханство сотрясали многочисленные восстания.

   Причины завоевания: Сокращение ханских земель, снижение доходов казны, рост недовольства населения.
   Восстания (1875 г.): Возглавлялись муллой Исхаком (Пулатхан). Худоярхан обратился за помощью к Кауфману.
   Договор Насриддинбека с Кауфманом (Маргилан): Насриддинбек объявляет себя «преданным подчиненным» императора, отрекается от внешней политики, обязуется уплатить контрибуцию (2 млн рублей), передает России земли по правому берегу Сырдарьи, города Наманган и Чуст.
   Подавление восстания Пулатхана: Основной удар – на Андижан (начало октября 1875 г.). Жестокие бои, разрушения. В январе 1876 г. город захвачен. Пулатхан и его сторонники казнены.
   Ликвидация ханства (19 февраля 1876 г.): Указ о ликвидации Кокандского ханства. Вместо него образована Ферганская область в составе Туркестанского генерал-губернаторства. Военным губернатором назначен М. Скобелев.
   Последствия: Завершение третьего этапа завоевания Средней Азии. Большая часть Средней Азии стала колонией Российской империи.

Ключевые события, даты и личности:
   1875 г.: Восстание Пулатхана.
   1876 г.: Ликвидация Кокандского ханства, образование Ферганской области.
   Личности: Худоярхан, Насриддинбек, Пулатхан, М. Скобелев, Кауфман.


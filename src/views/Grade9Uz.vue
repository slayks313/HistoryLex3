 Раздел III. Колониальная политика Российской империи в Туркестане

 !Политико-административная система управления Туркестанского генерал- губернаторства

Система управления формировалась постепенно, методом проб и ошибок, с учетом местных особенностей.

   «Временное положение об управлении Туркестанской областью» (6 августа 1865 г.): Управление осуществлялось военными. Система – «военно-народное управление». Губернатор обладал военной и гражданской властью. Область делилась на отделы, возглавляемые военными комендантами.
   «Положение о руководстве Туркестанским краем» (1886 г.): Укрепление административного управления и изменение землепользования.
   Структура управления:
       Высшее звено: Генерал-губернатор Туркестана (военный губернатор областей, командующий войсками, верховный судья). Канцелярия генерал-губернаторства – основной исполнительный орган.
       Области: Руководились военными губернаторами.
       Уезды: Руководились военными.
       Участки: Руководство – участковые приставы.
       Низшее звено: Сохранялись органы местного самоуправления (сельские аксакалы, казии, бии, начальники волостей, аулов).
   Политика: Сохранение низшего управленческого звена для снижения негативных последствий вмешательства в традиционный уклад, использования доверенных кадров.
   Переселение: Создание условий для переселенцев из центральных районов России, выделение земель, льгот. Русификация местного населения.
   Городское управление: В Ташкенте (1877 г.) – Городская дума (1/3 избиралась из Старого города, 2/3 – из Нового).
   Российское политическое агентство в Бухаре: Контроль над эмиратом.

Ключевые события, даты и личности:
   6 августа 1865 г.: «Временное положение об управлении Туркестанской областью».
   1867 г.: Создание Туркестанского генерал-губернаторства.
   1886 г.: Новое «Положение о руководстве Туркестанским краем».
   1877 г.: Введение Городского положения в Ташкенте.
   Личности: С. Духовской, К. Пален, К.П. фон Кауфман, Г. Розенбах.

 !Аграрная политика Российской империи в Туркестанском крае

Сельское хозяйство играло ведущую роль. Россия стремилась к полному контролю над земле- и водопользованием.

   «Положение 1886 года»: Земли, леса, недра Туркестанского края объявлены государственной собственностью России. Ликвидация крупных форм землевладения.
   Вакуфные земли: Предоставлялись проживающим на них, но требовалось согласие генерал-губернатора и уплата государственных податей. Доходы шли на ремонт зданий, содержание школ, но колониальные власти стремились подорвать статус духовенства.
   Переселенческая политика: Цель – укрепление колониальной системы. Переселенцам предоставлялись льготы (освобождение от налогов, льготные кредиты, земельные участки). Активный поток переселенцев из России.
   Последствия: Рост числа переселенцев, недовольство местного населения. Разделение городов на «новую» (русское население, администрация) и «старую» (местное население) части.

Ключевые события, даты и личности:
   1886 г.: Особый раздел «Поземельное устройство Туркестанского края».
   1875–1890 гг.: Переселение 1300 семей из России, образование 19 русских деревень.
   1891–1892 гг.: Образование еще 25 поселений переселенцев.
   Цель: Превращение Туркестана в сырьевой придаток России.

 !Создание судебной системы в Туркестане

Судебная система была приспособлена к колониальным интересам.

   Традиционная система: Суды казиев (на основе шариата) и биев (для кочевого населения). Казии избирались из уважаемых людей, не имели четко обозначенного участка.
   Реформы: Упразднение должности кази-каляна, уравнивание прав казиев. Ограничение функций и установление надзора над судами казиев и биев.
   Структура (согласно Положению 1867 г.): Суды казиев и биев, уездные суды, военно-судейские комиссии, судебные отделения областного правления, Судебная канцелярия генерал-губернаторства.
   Европейское население: Действовали русские суды.
   Изменения (конец XIX в.): Положение 1886 г. – упразднение уездных судов, введение участковых мировых судей. Областные суды сменили судебные отделения областных правлений. Введение должностей судебных следователей, прокуроров.
   1898 г.: Ликвидация областных судов, создание окружных судов и Ташкентской судебной палаты (высшая инстанция, подчинялась Сенату).
   Военно-полицейский режим: «Положение о мерах по поддержанию государственного правопорядка» (1881 г.) давало генерал-губернатору права вводить «усиленное» или «чрезвычайное» положение. Усиление полномочий исправников. Разоружение местного населения.

Ключевые события, даты и личности:
   Середина XIX в.: Суды казиев и биев.
   1867 г.: Введение судов казиев и биев, уездных судов.
   1886 г.: Изменения в судопроизводстве.
   1898 г.: Создание окружных судов и Ташкентской судебной палаты.
   1881 г.: «Положение о мерах по поддержанию государственного правопорядка».
   1892–1916 гг.: Туркестан попеременно в положении «усиленной» или «чрезвычайной охраны».

 !Социально-экономическое положение Туркестана во второй половине XIX – начале XX вв.

Колониальный режим насаждался военной силой и полицейской системой. Экономика края подчинена интересам империи.

   Железные дороги: Строительство (Красноводск–Ашхабад–Самарканд–Ташкент–Андижан, Оренбург–Ташкент) имело военно-стратегическое и экономическое значение.
   Экономика: Проникновение российского капитала, европейских товаров, новых технологий. Развитие хлопководства, горнорудного дела, первичной обработки сырья, маслобойной промышленности.
   Хлопководство: Основная отрасль, ориентированная на нужды текстильной промышленности России. Сокращение производства зерновых и других культур. Внедрение американских сортов хлопка. Строительство оросительных систем (магистральный канал в Сырдарье).
   Промышленность: Развитие первичной обработки хлопка. Производство готовой продукции – в России. В 1867–1900 гг. – 175 промышленных предприятий с ручным трудом (20 тыс. рабочих).
   Ремесло: Продукция местных ремесленников не выдерживала конкуренции с дешевыми российскими товарами, что привело к разорению многих мастеров.
   Торговля: Расширение торговой отрасли и сети железных дорог. Доходы шли в имперскую казну.
   Переселенческая политика: Увеличение численности русского населения, создание новых поселений. Разделение городов на «русскую» и «туземную» части.

Ключевые события, даты и личности:
   1888 г.: Первый состав достиг Самарканда по железной дороге.
   1899 г.: Железнодорожное полотно от Ташкента до Андижана.
   1905 г.: Завершение строительства железной дороги Оренбург–Ташкент.
   1867–1900 гг.: 175 промышленных предприятий.
   Цель: Превращение Туркестана в сырьевой придаток России.


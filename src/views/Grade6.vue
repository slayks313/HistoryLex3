  Заключение

История Древнего мира, охватывающая тысячелетия, заложила основы современной цивилизации. Достижения древности – колесо, алфавит, календарь, числовая система – используются нами до сих пор. Памятники культуры, произведения искусства и литературы народов древности продолжают восхищать и учить нас. Взаимодействие культур Востока и Запада способствовало развитию науки и искусства. История нашего края, Узбекистана, также имеет глубокие корни в древности, с формированием древних городов и государств, развитием ремесел и земледелия, героической борьбой предков за независимость.



 Начало античной истории

Древняя Греция располагалась на Балканском полуострове и островах Средиземного моря. Горный рельеф и отсутствие полноводных рек способствовали развитию мореплавания и торговли. Во II тыс. до н.э. возникли Минойская цивилизация на Крите и Микенская цивилизация в южной Греции. Вскоре они были разрушены вторжениями ахейцев и дорийцев. Греки активно занимались мореплаванием, совершая путешествия по Средиземному и Черному морям.


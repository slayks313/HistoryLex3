 !Социально-экономическая жизнь в Бухарском ханстве

   Землевладение: Все земли принадлежали государству (мамлакаи подшо). Формы: мульки хос (личные земли хана), мульки холис (купленные у государства), мульк (суюргал – условное владение, танхо – за заслуги), вакф (для религиозных учреждений).

   Реформы Абдуллахана II: Денежная реформа (учёт повышения цены на серебро, выпуск золотой монеты). Развитие сельского хозяйства через ирригацию (дамба Абдуллахана на Зарафшане). Благоустройство городов, строительство сардобов, рынков (тим Абдуллахана).
   Налоги и повинности: Хирадж (поземельный), Ихраджат (государственные расходы), Закят (имущество), Зобитона (правителю), Мадади лашкар (военный), Ясак (трудовая повинность).

 !Внешняя политика Бухарского ханства

   Отношения с Россией: Развивались со второй половины XVI века. Россия была заинтересована в торговле и выходе в Азию. Установлены торговые и дипломатические отношения (послы Абдуллахана II, Антони Дженкинсон). Важную роль играл вопрос паломничества хаджа.
   Отношения с Ираном (Сефевидами): Напряжённые из-за контроля над Хорасаном. Абдуллахан II пытался заключить союз с Османидами против Ирана, но безуспешно. Иран отделил Хорасан. В 1740 году Иран (Надиршах) сделал Бухарское ханство зависимым.
   Отношения с Индией (Бабуридами): Торговые и политические отношения. Абдуллахан II предлагал Акбаршаху разделить Иран, но тот отказался.
   Российская экспансия: В XVIII веке Россия усиливала давление (строительство крепостей, увеличение пошлин). Экспедиция Флорио Беневени (1721 г.) собирала информацию для будущего завоевания.

 !Культурная жизнь Бухарского ханства

   Наука: Развивались медицина (Мухаммад Мазид, Убайдулла Каххол), математика (Мухаммад Амин, Бобокалон Самарканди), астрономия (Мухаммад Хусейн Бухари). При Аштарханидах наука развивалась медленнее из-за религиозного фанатизма, но были и исключения (Мухаммад Шариф Бухари). При Субханкулихане развивалась медицина (клиника «Дор-уш-шифо»).
   Историческая наука: Труды Камалиддина Бинои и Мухаммад Салиха («Шейбани-наме»), Мухаммад Хайдара («Тарихи Рашиди»), Фазлуллаха ибн Рузбехана («Мехман-наме Бухары»), Хафиза Таниш Бухари («Абдулла-наме»).

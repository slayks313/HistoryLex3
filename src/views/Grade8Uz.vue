   Архитектура: Медресе Мир Араб, Абдуллахана, Кулбобо Кукельдаш, Надыр диванбеги (Бухара); Медресе Баракхан, Кукельдаш (Ташкент).
   Образование: Многоступенчатая система: махаллинские школы (2 года), медресе (21 год). Преподавались богословские науки, арифметика, юриспруденция, логика, музыка, поэтика.

 !Образование Бухарского эмирата

   Потеря независимости: В 1736–1740 гг. Иран (Надиршах) завоевал Бухару, сделав её зависимым государством. Абулфайзхан стал правителем-марионеткой.
   Приход к власти Мангытской династии: Племя мангытов, имевшее большой авторитет, постепенно заняло господствующее положение. Мухаммад Рахимбий, сын Мухаммада Хакимбия, в 1747 году казнил Абулфайзхана и возвёл на трон ханов-марионеток. В 1756 году объявил себя эмиром, основав Мангытскую династию.
   Укрепление центральной власти: Эмир Мухаммад Рахимбий подчинил вилаяты. Его дядя Даниялбий-аталык (1758–1785 гг.) укрепил войско, но столкнулся с восстаниями. Сын Даниялбия, Шахмурад (1785–1800 гг.), опираясь на народ, провёл реформы, укрепил торговлю и ремесленничество, снизил налоги, восстановил ирригационные системы.

 !Укрепление централизованной власти в эмирате

   Эмир Хайдар (1800–1826 гг.): Подавил противостояние местных правителей, но не смог обеспечить полного согласия. Вел борьбу за сохранение территории (Шахрисабз, Миянкаль, Ура-Тюбе). Столкнулся с народными восстаниями.
   Эмир Насрулла (1826–1860 гг.): Жестоко боролся за объединение вилаятов. Подчинил Шахрисабз и Китаб (30 лет борьбы). Ввёл культ личности эмира. Вел войны с Кокандским и Хивинским ханствами.
   Отношения с соседями: Взаимная нетерпимость, борьба за доминирование в регионе. Бухарский эмират захватил Кокандское ханство в 1842 г., но вскоре оно восстановило независимость.

 !Управление государством в Бухарском эмирате

   Государственный строй: Абсолютная монархия. Эмир – единоличный правитель и духовный лидер.
   Реформы: Ликвидированы системы Даргох и Диваны (при Аштарханидах). Усилена роль Кушбеги (главный визирь). Изменился статус Аталыка.
   Высшие должности: Кушбеги, Диванбеги (финансы), Аталык (ирригация), Парваначи (доставка указов), Инак (доведение приказов до служащих), Мулла (приём послов), Туксоба (хранение флага), Кукельдаш (безопасность), Мехтар (контроль доходов), Эшик агабаши, Мирахур, Каравулбеги, Шигаул, Мушриф, Дастарханчи, Китобдор, Додхох.
   Религиозные должности: Шейх-уль-ислам (глава духовенства), Казикалян (верховный судья), Муфтий (фетвы), Мухтасиб (контроль шариата).
   Административное деление: Вилаяты (беки), туманы, амлоки (амлокдор).

 !Вооружённые силы эмирата

   Регулярная армия: Создавалась с начала XIX века (эмир Хайдар – «навкария»). Реформа эмира Насруллы (1837 г.): сарбазы (пехота), навкар (конница), артиллеристы.
   Состав и управление: Сарбазы делились на подразделения. Верховная власть – эмир. Военный министр – тупчибаши. Воинские звания: понсадбаши, юзбаши, элликбаши, унбаши, токсаба. Казиаскер, мухтасиб, муфтий – в армии.
   Вооружение: Стрелковое оружие, копья, мечи, кольчуга, шлем. Пушки (50 шт.). Мастерские по изготовлению оружия (Самарканд, Карши).
   Снабжение и оплата: Зарплата (золотые монеты, батманы пшеницы), дополнительные выплаты, освобождение от налогов.

 !Социально-экономическая жизнь в Бухарском эмирате

   Территория и население: Около 2 млн человек (узбеки, таджики, туркмены и др.). Основная отрасль – сельское хозяйство (90% населения). Хлопководство, животноводство (овцеводство, каракуль).
   Сельское хозяйство: Искусственное орошение (чигирь). Примитивные орудия труда.
   Землевладение: Государственные земли (амлок), мульк (частная собственность – мульки хур), вакф. Суюргал и танхо – формы условного владения.
   Налоговая политика: Хирадж (поземельный), Танобона (садовые земли), Кош пули (налог на тягловый скот), Закят (скот, товары), Тола (чрезвычайный).

 !Города эмирата. Внутренняя и внешняя торговля

   Города: Бухара (столица с 1533 г., центр просвещения), Самарканд (восстанавливался после разрушений), Шахрисабз, Карши, Термез и др.
   Ремесленничество: Ткачество (олача, бязь, шёлк), кузнечное дело, ювелирное искусство, гончарное дело, резьба по дереву, изготовление ковров.
   Внутренняя торговля: Базары – центры торговли, общественной и культурной жизни. Цены и весы строго контролировались.
   Внешняя торговля: Связи с Индией, Афганистаном, Ираном, Китаем, Россией. Рост торговли с Россией (хлопок, текстиль).

 !Внешняя политика Бухарского эмирата

   Основные принципы: Стремление к доминированию в Средней Азии, укрепление в Хорасане, торговые отношения с Россией и другими государствами, организация паломничества хаджа через Астрахань.
   Отношения с Россией: Развитие торговых и дипломатических связей. Россия стремилась к завоеванию Средней Азии.
   Отношения с Ираном: Напряжённые из-за Хорасана. Бухара стала зависимой от Ирана в 1740 г.
   Отношения с Инди|ей: Торговые связи.
   Конкуренция России и Великобритании: Борьба за влияние в Средней Азии. Британские экспедиции (У. Муркфорд, А. Бёрнс) изучали эмират. Россия готовилась к завоеванию.

 !Культурная жизнь Бухарского эмирата

   История: Труды Мухаммада Якуба ибн Даниялбия («Гулшан ал-мулук»), Мирзы Шамса Бухари («Баёни баъзи хаводисоти Бухоро...»), Мир Алима Бухари («Фатхномаи султоний»).
   Литература: Мухаммаднияз Нишати («Хусн-и дил», «Спор птиц»), Мужрим Обид, Вола, Ахмад Даниш.
   Изобразительное искусство: Каллиграфия и миниатюра (Мирмаъсум Оламиян, Мирза Садык Джандари).
   Музыка: «Шашмаком» (шедевр нематериального наследия ЮНЕСКО).
   Образование: Мактабхона (начальные школы), Медресе (среднее и высшее). Преподавались богословские науки, арабский язык, правоведение, история, литература, математика.



 Гла#ва III. Хивинское ханство

 !Образование Хивинского ханства

 [img: хивинское ханство]

   Борьба за независимость: После завоевания Хорезма Шейбаниханом (1505 г.) и шахом Исмаилом (1510 г.), хорезмийцы боролись за независимость. Лидеры – Умар-кори и Саид Низамитдин-каттол.
   Восстановление независимости: В 1512 году на престол Хивы возведён Эльбарсхан, сын султана Берка из династии Шейбанидов. Столица перенесена из Вазира в Ургенч. Хивинское ханство стало вторым узбекским государством.
   Династия Арабшахидов: Шейбаниды Хивы назывались Арабшахийцами (потомки Арабшаха).
   Кризис XVI века: Изменение русла Амударьи, экономический кризис, войны с Бухарой, тяжёлые налоги.
   Отношения с Бухарой: Обострённые из-за территориальных претензий и вражды династий. Убайдуллахан (Бухара) подчинил Хиву в 1537–1538 гг., но независимость была восстановлена. Абдуллахан II (Бухара) окончательно подчинил Хиву в 1598 г., но после его смерти она вновь стала независимой.

 !Политическая обстановка в Хивинском ханстве в XVII – первой половине XVIII веков

   Усиление племенных вождей: Хан не мог принимать решения самостоятельно. Политическая раздробленность.
   Перенос столицы в Хиву: Араб Мухаммадхан (1602–1623 гг.) перенёс столицу из Ургенча в Хиву из-за изменения русла Амударьи.
   Междоусобицы: Сыновья Араб Мухаммадхана подняли мятеж. Асфандиярхан (1623–1643 гг.) пришёл к власти с помощью шаха Ирана. Абулгази Баходырхан (с 1644 г.) провёл реформы для централизации власти, разделив племена на 4 группы.
   Отношения с Бухарой: Продолжали оставаться напряжёнными. Абулгазихан противостоял нападениям Бухары. В 1662 г. заключено перемирие. При Анушахане (сыне Абулгази) отношения вновь обострились.
   Зависимость от России: Шахнияз (наиб Хивы) в 1700 г. просил покровительства России. Экспедиция А. Бековича-Черкасского (1714 г.) – первая попытка России распространить влияние.
   Шергазихан (1715–1727 гг.): Обострение раздробленности и конфликтов с Бухарой.
   Завоевание Ираном: Надиршах (Иран) в 1740 г. захватил Хиву, установив иранскую систему управления. Продолжались внутренние распри («игры ханов»).

 !Приход к власти Кунгратской династии в Хивинском ханстве

   Необходимость централизации: Экономика страдала от раздробленности, войн и нападений. Народ разорялся.
   Возвышение племени кунграт: С 1760-х гг. власть перешла к предводителям кунгратов. Мухаммад Амин (инак) в 1770 г. изгнал туркменские племена и установил контроль над ханством.
   Официальное установление династии: Мухаммад Амин (1770–1790 гг.) и его сын Аваз (1790–1804 гг.) правили как инаки. В 1804 году сын Аваза Элтузархан объявил себя ханом, официально основав Кунгратскую династию, правившую до 1920 г.
   Реформы Мухаммада Рахимхана I (правил после смерти Элтузархана): Объединение ханства, политическая стабильность, расширение территории. Создание таможенной службы, чеканка золотых монет (1809 г.). Учреждение Олий Кенгаш (Верховный Совет) и Кичик Кенгаш (Малый Совет).

 !Государственное управление в Хивинском ханстве

   Территория и население: Около 800 тыс. человек (узбеки – 65%, туркмены – 26%, каракалпаки, казахи, арабы). Столица – Хива (более 20 тыс. жителей).
   Государственный строй: Абсолютная монархия. Хан – верховный правитель. Функционировали Олий и Кичик Кенгаши.
   Административное деление: 23 единицы: 20 бекств (беки), 2 наиблика (наибы), Хива и окрестности (хан). Бекства делились на города (хакимы) и сёла (аксакалы).
   Высокопоставленные должности: Мехтар (премьер-министр), Кушбеги (сбор налогов, финансы, войско), Ясаулбаши (командующий армией), Диванбеги (финансы), Мираб, Парваначи, Кутлау, Шугавул, Махрам.
   Судопроизводство: Кази (судьи), Муфтий (фетвы), Аълам (руководитель муфтиев), Казикалян, Кази орды, Кази хосса, Кази аскер, Раис.

 !Социально-экономическая жизнь в Хивинском ханстве

   Землевладение: Государственная (ханская, тарханные земли), частная (мульк), вакфная. Форма атлык – временное владение землёй для конников-туркмен.
   Сельское хозяйство: Тяжёлые условия из-за нехватки воды. Выращивание пшеницы, риса, винограда, фруктов.
   Налоги и повинности: Салгут (поземельный), Закят (ремесленники, торговцы, скотоводы), Барот (единовременный сбор налогов), Бегар (общественная работа).
   Города: Старый Ургенч (древняя столица, разорена монголами), Новый Ургенч (основан Абулгазиханом), Хива (столица с XVII в., «город тысячи куполов», Ичан-кала – музей-заповедник, Дишан-кала – укреплённая часть).
   Торговля: Внутренняя (базары – центры жизни). Внешняя – с Ираном, Индией, Китаем, Россией. Экспорт каракуля, ковров, хлопка, шёлка. Импорт текстиля, сахара, железа.

 !Внешняя политика Хивинского ханства

   Отношения с азиатскими государствами: С Бухарой (обострённые, войны за Мерв), Ираном, Афганистаном, Кокандом.
   Отношения с Россией: Взаимная заинтересованность в торговле. Россия стремилась к завоеванию Средней Азии. «Акт об обязательствах» (1842 г.) установил мирные отношения, но Россия продолжала строить крепости.
   Конкуренция России и Великобритании: Великобритания стремилась укрепить влияние через торговлю. Россия опасалась британского влияния. Соглашение о сферах влияния (Афганистан как буфер).

 !Культурная жизнь Хивинского ханства

   Литература: Мунис Хорезми («Девони Мунис», «Ошиклар дусти»), Агахи (племянник Муниса, «Ошиклар тумори», переводчик), Абулгази Баходырхан («Шажараи турк», «Дастурул-амал»), Мунис («Фирдавс-ул-икбол»), Агахи («Риёз уд-давла»).
   Искусство: Каллиграфия и миниатюра (Хамадони, Мухаммад Юсуф Рази, Бабаджан Санои).
   Образование: Начальные школы (мактабхона), Медресе (высшее образование). Преподавались теология, арабский язык, грамматика, логика, математика, география, история.

 !Источники по истории Хивинского ханства

   Местные историки: Архивные документы (возвращены в Ташкент в 1962 г.), сочинения Абулгази Баходырхана («Шажараи турк»), Муниса («Фирдавс-ул-икбол»), Агахи («Риёз уд-давла»).
   Российские дипломаты и учёные: И. Д. Хохлов (1819–1822 гг.), Флорио Беневени (1725 г.), Н. Н. Муравьёв-Карский (1819 г.), Г. Данилевский и Р. Базинер («Хива и другие города...»), Н. Игнатьев (1858 г.), Н. Васильевский, С. Жуковский.
   Европейские путешественники: А. Дженкинсон (XVI в.), А. Вамбери.
   Узбекские учёные: И. Муминов, Я. Гулямов, М. Юлдашев, Д. Алимова.

 !Каракалпаки в период с XVI века до первой половины XIX века
 

   Становление народа: Началось в VIII веке, основано печенежскими племенами. Государство с центром в Кердере (XIII в.) было ликвидировано Чингисханом.
   Переселения: Часть каракалпаков переселилась в Заволжье («чёрные клобуки»). Из-за изменения русла Амударьи – к Волге, Уралу, Сырдарье.
   В составе Ногайского ханства (XV–XVI вв.): Имели собственное управление. После распада – в составе Малого жуза казахов (государство с центром в Джан-кенте).
   Отношения с Россией: В 1731 г. «нижние» каракалпаки и Малый жуз стали подданными России, платя дань казахскому хану. Это спасло их от Надиршаха.
   Отношения с Хивой: Подчинение Хивинскому ханству в 1735 г. (узбеки Приаралья и каракалпаки). Восстание под предводительством Ойдустбия (1827 г.) было подавлено. В 1859 г. хивинский хан Саид Мухаммадхан дал им право на самоуправление.
   Общественная жизнь: Родоплеменные отношения, сильное расслоение. Во главе родов – бии, воинских частей – батыры.
   Хозяйство: Земледелие (Приаралье, между Сырдарьёй и Амударьёй), животноводство, рыболовство, охота.

 !Культура каракалпакского народа

   Образ жизни: Переход от кочевого к оседлому. Жили в юртах и глиняных домах, строили крепости и города (Кунграт, Чимбай, Ходжейли).
   Фольклор: Богатейшее устное народное творчество (20-томное издание). Герои – Амирбек лакки (аналог Насриддина Афанди), героические дастаны («Кирккиз», «Алпамыш», «Эдыгей»). Народные песни.
   Письменная литература: Джиен Джиров (бахши, сатирик), Кунходжа Ибрагим, Аджинияз («Бузатов»), Бердах (основатель литературы, «Амангельды», «Потомки»).
   Искусство: Ковроткачество, вышивка (узоры в виде волн), резьба по дереву.
   Музыка: Песни исполнялись под кобуз, дутар, гижжак, сурнай, нагара. Джиравы (сказители), бахши, киссахонлар.
   Образование: Начальные школы (мактабхона), медресе (Хива, Бухара).



 Глав#а IV. Кокандское ханство в XVIII – первой половине XIX веков

 !Образование Кокандского ханства
 

   Объединение Ферганской долины: В начале XVIII века ослабление Бухарского ханства и нападения джунгар способствовали объединению рода минг под руководством Шахрухбия. В 1719 году минги взяли власть в Фергане.
   Статус государства: Изначально рассматривалось как часть Бухарского ханства, Шахрухбий имел титул аталыка. Своей денежной единицы не было.
   Развитие Коканда: Сын Шахрухбия Абдурахимбий сделал Коканд столицей, присоединил Ходжанд и Ура-Тюбе. После его смерти – Абдукаримбий (1733–1750 гг.).
   Союз против джунгар: Минги, кипчаки, киргизы и др. объединились против джунгарских нашествий (1740, 1745 гг.).
   Нарбутабий (1763–1798 гг.): Подавил мятежи, подчинил Ош и Узген. В 1776 г. выпустил первую денежную единицу – фалс. Укрепил государство.

 !Политическое положение Кокандского ханства во второй половине XVIII – первой половине XIX веков

   Алимхан (1798–1810 гг.): Возведён на трон оппозиционной группой. В 1805 г. объявил себя ханом, выпустил серебряную монету «динар». Расширил территорию (Ташкент, Чимкент, Туркестан). Установил торговые отношения с Россией. Жестокие действия по укреплению власти вызвали недовольство. Погиб в результате заговора.
   Умархан (1810–1822 гг.): Укрепил власть, устранил претендентов на трон. Выпустил монеты с титулом «Амир аль-муслимин». Признан независимым от Бухары османским султаном. Расширил территорию ханства.
   Мухаммад Алихан (Мадалихан) (1822–1841 гг.): Значительно расширил территорию. Преследовал приближённых Умархана, что привело к разрушению союза с аристократией. Обвинен духовенством в аморальности. Отрекся от престола в пользу брата Султан Махмудхана.
   Завоевание Бухарой: В 1842 г. эмир Насрулла (Бухара) завоевал Коканд, казнив Мадалихана и его брата.
   Восстановление независимости и господство кипчаков: В 1842 г. восстание свергло власть Бухары. На трон возведён Шералихан. Кипчаки (предводитель Мусулманкул) получили высокий статус. В 1845 г. Мусулманкул возвёл на трон своего зятя Худоярхана (сына Шералихана). Началось правление кипчаков.
   Ликвидация господства кипчаков: В 1852 г. Худоярхан с помощью ташкентского войска организовал резню кипчаков и казнил Мусулманкула. Внутренняя нестабильность продолжилась.

 !Государственное управление в Кокандском ханстве

   Территория и население: К середине XIX века – крупнейшее государство Средней Азии. Границы с Россией, Хивой, Бухарой, Ираном, Афганистаном. Население – 3 млн человек (узбеки, таджики, киргизы, кашгары).
   Государственный строй: Абсолютная монархия. Хан – верховный правитель. Опирался на духовенство, родственников, племенных вождей, чиновников, военных.
   Высшие должности: Мингбаши (главный визирь, главнокомандующий), Диванбеги (финансы), Мирза (учёт доходов), Дастурханчи (подача блюд), Мехтар (заместитель диванбеги), Шигавул, Инак, Аталык, Додхох, Кушбеги, Парваначи, Хазиначи.
   Духовенство (Уламо): Шейх-уль-ислам, Казикалян, Ходжи калян, Накиб, Кази аскер, Муфтий, Кази аълам, Раис.
   Административное деление: 15 бекств (беки), саркоры, аксакалы, эмины. Бекства – военные округа.

 !Вооружённые силы Кокандского ханства

   Регулярная армия: Создана Алимханом (1805 г.) – 10 тыс. «навкария» (горные таджики), вооружённые огнестрельным оружием. При Умархане – 12 тыс. Худоярхан привёз качественное оружие из Османской империи.
   Нерегулярные войска: Килкуйрук, Караказан, Карачерик (вспомогательные).
   Состав и управление: 30 тыс. аскеров (30–40-е гг. XIX в.). Сипах (конница – 20 тыс.), тупчи (артиллерия), сарбоз (пехота), галабатиры и стрелки. Амири лашкар (главнокомандующий). Мингбаши, Амири лашкар, Кушбеги, Батырбаши, Валий, Калъабан, Курбаши, Ёвар, Тупчибаши, Токсаба, Понсадбаши.
   Снабжение: Налоговые доходы, специальный налог «милтик пули». Зарплата (монеты, пшеница).
   Крепости: Укрепление границ (Ташкент, Туркестан, Чимкент, Ура-Тюбе). Крепость Коканда имела две стены.

 !Социально-экономическая жизнь в Кокандском ханстве

   Землевладение: Государственные (амлакские – хос мульк, такик ер, чек ерлар), частные (мульк – за заслуги или продажа, танхо ерлари – военным), вакфные земли.
   Население: Мардикоры (поденщики на ханских землях), Чорикоры (наёмные работники за 1/4 урожая), Коранда (издольщики за 1/2 урожая).
   Налоги и повинности: Хирадж (поземельный), Закят (скотоводы, торговцы), Курабаши, Тутун хаки, Йигим солик (налоги кочевников). Бегар (общественная работа).
   Сельское хозяйство: Искусственное орошение. Основные культуры – пшеница, хлопок. Развито хлопководство (экспорт в Россию).
   Ремесленничество: Медники, ювелиры, ткачи, оружейники, ковроделы и др.
   Торговля: С Бухарой, Хивой, Кашгаром, Индией, Афганистаном, Ираном, Россией. Коканд – крупный торговый центр.
   Города: Коканд (столица, центр культуры), Андижан (центр Ферганской долины), Ташкент (древний город, центр ремёсел).

 !Внешняя политика Кокандского ханства

   Отношения с Китаем: Напряжённые. Китай подчинил Восточный Туркестан. Коканд поддерживал национально-освободительное движение в Кашгаре. Договор 1832 г. в Пекине урегулировал торговые и пограничные вопросы.
   Отношения с Бухарой: Враждебные, борьба за территории (Ура-Тюбе, Ходжанд, Ташкент). Бухара стремилась включить Коканд в свой состав.
   Отношения с Россией: Попытки установить торговые и военные связи (послы Умархана, Мадалихана). Россия претендовала на покровительство казахам, что препятствовало развитию отношений. Строительство российских крепостей (Раим) обострило отношения.

 !Культурная жизнь Кокандского ханства

   Наука и культура: Коканд, Маргилан, Андижан – центры культуры. При Умархане (начало XIX в.) Коканд стал центром науки и культуры.
   Литература: Умархан (псевдоним Амири), Надирабегим (супруга Умархана), Фазли Намангани («Маджмуаи шоирон»), Махмур (сатирик, «Хапалак»), Гульхани (баснописец, «Зарбулмасал»), Увайси (поэтесса), Надира (поэтесса, гос. деятель), Боборахим Машраб (суфий, поэт-сатирик).
   Исторические труды: Абдулкарим Фазли Намангани («Умарнаме»), Мирза Каландар Мушриф Исфаранги («Шохномаи нусратпаём»), Мухаммад Хакимхан тура («Мунтахаб ут-таворих»), Мухаммад Салих («Тарихи жадидаи Ташкент»).
   Архитектура: Медресе Нарбутабия (Мир), ансамбль Дахмаи Шохон, мавзолей Нарбутабия, дворец Худоярхана (Коканд). Медресе Баракхан и Кукельдаш (Ташкент).

 !Образование в Кокандском ханстве

   Школы (Мактабхона): Начальное образование, грамота (арабский алфавит), математика, основы ислама, этика. Обучение детей 5–12 лет (5–7 лет). Учителя – имамы, выпускники медресе.
   Медресе: Среднее и высшее образование (13–15 летние ученики, 15–20 лет обучения). Преподавались богословские науки (калом, кироат, хадис, фикх), арабская грамматика, логика, каллиграфия, коммерция, история, литература, география, математика. Расходы покрывались за счёт вакфа.

